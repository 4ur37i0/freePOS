       document.addEventListener('DOMContentLoaded', () => {
    // --- Obtención de referencias a elementos del DOM ---
    // Se obtienen después de que el DOM esté completamente cargado.
    const btnObtenerUbicacion = document.getElementById('btnObtenerUbicacion');
    const infoUbicacion = document.getElementById('infoUbicacion'); // Definido aquí, accesible globalmente dentro de este script

    // --- Verificaciones iniciales ---
    if (!btnObtenerUbicacion) {
        console.error("Error: El botón con id 'btnObtenerUbicacion' no fue encontrado.");
        if (infoUbicacion) {
            infoUbicacion.textContent = "Error de configuración: Botón no encontrado.";
        }
        return; // Detener la ejecución si el botón no existe
    }

    if (!infoUbicacion) {
        console.error("Error: El elemento con id 'infoUbicacion' no fue encontrado.");
        // No se puede mostrar feedback al usuario si este elemento falta.
        return;
    }

    // --- Event Listener para el botón ---
    btnObtenerUbicacion.addEventListener('click', () => {
        if (navigator.geolocation) {
            infoUbicacion.innerHTML = "Solicitando ubicación..."; // Actualiza el mensaje

            navigator.geolocation.getCurrentPosition(
                mostrarPosicion,
                manejarError,
                {
                    enableHighAccuracy: true, // Intenta obtener la ubicación más precisa
                    timeout: 15000,          // Tiempo máximo para obtener la ubicación (15 segundos)
                    maximumAge: 0            // No usar una ubicación en caché
                }
            );
        } else {
            infoUbicacion.textContent = "La geolocalización no es soportada por este navegador.";
        }
    });

    // --- Función para manejar el éxito de la geolocalización ---
    function mostrarPosicion(posicion) {
        const latitud = posicion.coords.latitude;
        const longitud = posicion.coords.longitude;
        const precision = posicion.coords.accuracy;
        // Formatear el timestamp para MySQL DATETIME (YYYY-MM-DD HH:MM:SS)
        const clientTimestamp = new Date(posicion.timestamp).toISOString().slice(0, 19).replace('T', ' ');

        // Mostrar la información en el frontend
        infoUbicacion.innerHTML = `
            <b>Ubicación Obtenida:</b><br>
            Latitud: ${latitud.toFixed(6)} <br>
            Longitud: ${longitud.toFixed(6)} <br>
            Precisión: ${precision.toFixed(2)} metros <br>
            Timestamp (Cliente): ${clientTimestamp} <br>
            <hr>
            Enviando al servidor...
        `;

        // Preparar datos para enviar al backend
        const datosParaBackend = {
            latitud: latitud,
            longitud: longitud,
            precision: precision,
            timestamp: clientTimestamp
            // Podrías añadir más datos aquí si los tuvieras (ej. ID de usuario)
        };

        // Enviar datos al backend usando fetch
        fetch('/api/register-ubi', { // Asegúrate que esta URL es la correcta para tu endpoint PHP
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                // Si usas autenticación basada en tokens, añádelos aquí:
                // 'Authorization': 'Bearer TU_TOKEN_JWT_AQUI'
            },
            body: JSON.stringify(datosParaBackend)
        })
        .then(response => {
            // Primero, verifica si la respuesta es OK (status en el rango 200-299)
            if (!response.ok) {
                // Intenta obtener el mensaje de error del cuerpo JSON si existe
                return response.json().then(errorData => {
                    throw new Error(errorData.message || `Error del servidor: ${response.status} ${response.statusText}`);
                }).catch(() => {
                    // Si no hay cuerpo JSON o falla el parseo, usa el statusText
                    throw new Error(`Error del servidor: ${response.status} ${response.statusText}`);
                });
            }
            return response.json(); // Convierte la respuesta del servidor (que se espera sea JSON)
        })
        .then(data => {
            console.log('Respuesta del servidor:', data);
            if (data.status === 'success') {
                infoUbicacion.innerHTML += `<br><b>Respuesta del Servidor:</b> ${data.message}`;
            } else {
                infoUbicacion.innerHTML += `<br><b style="color:red;">Error del Servidor:</b> ${data.message || 'Respuesta no exitosa.'}`;
            }
        })
        .catch(error => {
            console.error('Error al registrar ubicación:', error);
            infoUbicacion.innerHTML += `<br><b style="color:red;">Error de Conexión o Proceso:</b> ${error.message}`;
        });
    }

    // --- Función para manejar errores de geolocalización ---
    function manejarError(error) {
        let mensajeError = "<b>Error al obtener la ubicación:</b> ";
        switch(error.code) {
            case error.PERMISSION_DENIED:
                mensajeError += "El usuario denegó la solicitud de geolocalización.";
                break;
            case error.POSITION_UNAVAILABLE:
                mensajeError += "La información de ubicación no está disponible.";
                break;
            case error.TIMEOUT:
                mensajeError += "La solicitud para obtener la ubicación del usuario ha caducado.";
                break;
            case error.UNKNOWN_ERROR:
                mensajeError += "Ha ocurrido un error desconocido.";
                break;
        }
        infoUbicacion.innerHTML = mensajeError; // Sobrescribe el contenido con el error
        console.error("Error de geolocalización (código " + error.code + "): ", error.message);
    }
});
